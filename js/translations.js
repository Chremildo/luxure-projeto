// =======================================
// SITE TRANSLATION SYSTEM
// Updates visible page text when language changes
// =======================================
export const initTranslations = () => {
  const applyLanguage = (language) => {
    const dictionary = translations[language] || translations.en;
    document.documentElement.lang = language === "pt" ? "pt-BR" : language;
    localStorage.setItem("lennan-language", language);

    document.querySelectorAll("[data-i18n]").forEach((element) => {
      const value = dictionary[element.dataset.i18n];
      if (value) element.textContent = value;
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
      const value = dictionary[element.dataset.i18nPlaceholder];
      if (value) element.setAttribute("placeholder", value);
    });

    document.querySelectorAll("[data-language-select], [data-chat-language]").forEach((select) => {
      select.value = language;
    });
  };

  window.JamesLennanTranslate = applyLanguage;

  document.addEventListener("change", (event) => {
    const select = event.target.closest("[data-language-select], [data-chat-language]");
    if (select) applyLanguage(select.value);
  });

  document.addEventListener("james-language-change", (event) => {
    if (event.detail?.language) applyLanguage(event.detail.language);
  });

  applyLanguage(localStorage.getItem("lennan-language") || "en");
};
